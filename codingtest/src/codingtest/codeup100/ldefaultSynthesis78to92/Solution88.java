import java.util.Scanner;

public class Solution88 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int x = sc.nextInt();
		for (int i=0; i <= x ; i++) {
			if(i%3!=0)
				System.out.print(i + " ");
				
		}
	}

}
